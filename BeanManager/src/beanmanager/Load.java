    public Load(int formulario) {
        
        initComponents();
        setResizable(false); //Quitar Resize
        setLocationRelativeTo(null);//Centra pantalla
        setLayout(null); // Libre seleccion de tama√±o
        getContentPane().setBackground(Color.decode("#FFFFFF"));//Colocamos fondo blanco
        opcion=formulario;
        Timer timer= new Timer();
        timer.schedule(new TimerTask() {
            @Override
            public void run() {
              timerC();
            }
          },100);
                
        
    }
    public void timerC()
    {
        switch(opcion)
        {
            case 1:
                IndexSolicitud proyectos = new IndexSolicitud(session);
                proyectos.padre = padre;
                proyectos.setVisible(true);
                break;
            case 2:
               InicioAjustes u=new InicioAjustes(session);
               u.session=session;
               u.setVisible(true);
                break;
            case 3:
                MenuUsuarios menuUsr = new MenuUsuarios(session);
                menuUsr.padre=padre;
                menuUsr.setVisible(true);
                break;
            case 4:
               IndexProyecto a=new IndexProyecto(session);
               a.session=session;
               a.setVisible(true);
               
             break;
            case 5:
               menuInicio m=new menuInicio();
               m.setVisible(true);
             break;
        }
        this.setVisible(false);
     
    }

    
    
